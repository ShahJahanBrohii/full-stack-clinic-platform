/**
 * Export utility functions for admin data export features
 * Supports CSV export for bookings, patients, and other data
 */

/**
 * Convert array of objects to CSV string
 * @param {Array} data - Array of objects to convert
 * @param {Array} headers - Column headers to include
 * @returns {string} CSV formatted string
 */
export function arrayToCSV(data, headers) {
  if (!data || data.length === 0) return '';

  // Header row
  const headerRow = headers.map((h) => `"${h.label}"`).join(',');

  // Data rows
  const dataRows = data.map((item) =>
    headers
      .map((h) => {
        let value = item[h.key];
        // Handle various data types
        if (value === null || value === undefined) return '';
        if (typeof value === 'object') value = JSON.stringify(value);
        // Escape quotes and wrap in quotes if needed
        return `"${String(value).replace(/"/g, '""')}"`;
      })
      .join(',')
  );

  return [headerRow, ...dataRows].join('\n');
}

/**
 * Download CSV file
 * @param {string} csv - CSV content
 * @param {string} filename - Name of the file to download
 */
export function downloadCSV(csv, filename) {
  const blob = new Blob([csv], { type: 'text/csv;charset=utf-8;' });
  const link = document.createElement('a');
  const url = URL.createObjectURL(blob);

  link.setAttribute('href', url);
  link.setAttribute('download', filename);
  link.style.visibility = 'hidden';

  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
}

/**
 * Export bookings to CSV
 * @param {Array} bookings - Array of booking objects
 */
export function exportBookingsToCSV(bookings) {
  const headers = [
    { key: 'patientName', label: 'Patient Name' },
    { key: 'patientEmail', label: 'Email' },
    { key: 'serviceName', label: 'Service' },
    { key: 'date', label: 'Date' },
    { key: 'timeSlot', label: 'Time' },
    { key: 'status', label: 'Status' },
    { key: 'paymentMethod', label: 'Payment' },
    { key: 'price', label: 'Price' },
    { key: 'notes', label: 'Notes' },
  ];

  const csv = arrayToCSV(bookings, headers);
  const timestamp = new Date().toISOString().slice(0, 10);
  downloadCSV(csv, `bookings_${timestamp}.csv`);
}

