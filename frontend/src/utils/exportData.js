/**
 * Generate admin report with statistics
 * @param {Object} stats - Stats object with totals
 * @returns {string} Formatted report text
 */
export function generateAdminReport(stats) {
  const timestamp = new Date().toLocaleString('en-PK');

  return `
ADMIN REPORT
Generated: ${timestamp}

=== BOOKING STATISTICS ===
Total Bookings: ${stats.totalBookings || 0}
Confirmed: ${stats.confirmed || 0}
Pending: ${stats.pending || 0}
Completed: ${stats.completed || 0}
Cancelled: ${stats.cancelled || 0}

=== PATIENT STATISTICS ===
Total Patients: ${stats.totalPatients || 0}
Active: ${stats.activePatients || 0}
Inactive: ${stats.inactivePatients || 0}

=== FINANCIAL ===
Total Revenue: PKR ${(stats.totalRevenue || 0).toLocaleString('en-PK')}
This Month: PKR ${(stats.revenueThisMonth || 0).toLocaleString('en-PK')}

=== SERVICES ===
Total Services: ${stats.totalServices || 0}
Active Services: ${stats.activeServices || 0}

Generated by Ali's Clinic Admin Panel
`;
}

/**
 * Generate and download admin report
 * @param {Object} stats - Statistics object
 */
export function downloadAdminReport(stats) {
  const report = generateAdminReport(stats);
  const blob = new Blob([report], { type: 'text/plain;charset=utf-8;' });
  const link = document.createElement('a');
  const url = URL.createObjectURL(blob);

  const timestamp = new Date().toISOString().slice(0, 10);
  link.setAttribute('href', url);
  link.setAttribute('download', `admin_report_${timestamp}.txt`);
  link.style.visibility = 'hidden';

  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
}
